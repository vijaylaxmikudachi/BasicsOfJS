// Taking input from the user using prompt
let name = prompt("What is your name?");
let age = prompt("How old are you?");

// Processing input (optional)
let ageNextYear = parseInt(age) + 1;

// Output using console.log
console.log("Hello, " + name + "! You are currently " + age + " years old.");
console.log("Next year, you will be " + ageNextYear + " years old.");

// Output using alert
alert("Hello, " + name + "! You are currently " + age + " years old.");
alert("Next year, you will be " + ageNextYear + " years old.");
